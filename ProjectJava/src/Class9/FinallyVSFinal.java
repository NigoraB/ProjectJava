package Class9;

public class FinallyVSFinal {
    /**
     *
     * finally:
     *      is code block with try-catch
     *      which always executes irrespective of exception occurs.
     *
     *
     * final:
     *      is a keyword, which we use to make variable/class value constant.
     *      In other words, if a variable is final, user cannot change it's value.
     *
     */

    public static void main(String[] args) {
        final int num;
        num = 5;
    }
}
